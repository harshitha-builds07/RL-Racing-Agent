class Car:

    def __init__(self):

        # Start Position (Bottom of the track)
        self.x = CENTER_X
        self.y = CENTER_Y + (OUTER_RADIUS_Y + INNER_RADIUS_Y) / 2

        # Facing Right
        self.angle = 0

        # Physics
        self.speed = 0

        self.max_speed = 4
        self.acceleration = 0.15
        self.brake_force = 0.25
        self.friction = 0.03

        self.turn_speed = 4

    def reset(self):

        self.__init__()

def update_car(car, action):

    # -------------------------
    # Actions
    # -------------------------

    if action == 1:          # Accelerate
        car.speed += car.acceleration

    elif action == 2:        # Brake
        car.speed -= car.brake_force

    elif action == 3:        # Left
        car.angle -= car.turn_speed

    elif action == 4:        # Right
        car.angle += car.turn_speed


    # -------------------------
    # Speed Limits
    # -------------------------

    car.speed = np.clip(car.speed, 0, car.max_speed)


    # -------------------------
    # Friction
    # -------------------------

    if car.speed > 0:
        car.speed -= car.friction


    # -------------------------
    # Move Car
    # -------------------------

    rad = np.deg2rad(car.angle)

    car.x += car.speed * np.cos(rad)

    car.y += car.speed * np.sin(rad)

car = Car()

