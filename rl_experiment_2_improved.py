class RaceEnv(gym.Env):

    def __init__(self):

        super().__init__()

        self.car = Car()

        # Observation:
        # x, y, speed, angle,
        # front sensor, left sensor, right sensor

        self.observation_space = spaces.Box(
            low=np.array([0,0,0,-180,0,0,0],dtype=np.float32),
            high=np.array([300,300,self.car.max_speed,180,100,100,100],dtype=np.float32)
        )

        # Actions
        # 0 Nothing
        # 1 Accelerate
        # 2 Brake
        # 3 Left
        # 4 Right

        self.action_space = spaces.Discrete(5)



    # ------------------------------------------------
    # Distance Sensor
    # ------------------------------------------------

    def get_sensor_distance(self, angle_offset):

        angle = np.deg2rad(self.car.angle + angle_offset)

        distance = 0

        while distance <= 100:

            px = self.car.x + distance*np.cos(angle)
            py = self.car.y + distance*np.sin(angle)

            outer = ((px-CENTER_X)/OUTER_RADIUS_X)**2 + \
                    ((py-CENTER_Y)/OUTER_RADIUS_Y)**2

            inner = ((px-CENTER_X)/INNER_RADIUS_X)**2 + \
                    ((py-CENTER_Y)/INNER_RADIUS_Y)**2

            if not (outer <= 1 and inner >= 1):
                break

            distance += 2

        return distance



    # ------------------------------------------------
    # Observation
    # ------------------------------------------------

    def get_observation(self):

        front = self.get_sensor_distance(0)
        left = self.get_sensor_distance(-45)
        right = self.get_sensor_distance(45)

        return np.array([
            self.car.x,
            self.car.y,
            self.car.speed,
            self.car.angle,
            front,
            left,
            right
        ],dtype=np.float32)



    # ------------------------------------------------
    # Reset
    # ------------------------------------------------

    def reset(self, seed=None, options=None):

        super().reset(seed=seed)

        self.car.reset()

        return self.get_observation(), {}



    # ------------------------------------------------
    # Step
    # ------------------------------------------------

    def step(self, action):

        old_x = self.car.x
        old_y = self.car.y

        update_car(self.car, action)

        x = self.car.x
        y = self.car.y

        outer = ((x-CENTER_X)/OUTER_RADIUS_X)**2 + \
                ((y-CENTER_Y)/OUTER_RADIUS_Y)**2

        inner = ((x-CENTER_X)/INNER_RADIUS_X)**2 + \
                ((y-CENTER_Y)/INNER_RADIUS_Y)**2

        on_track = (outer <= 1) and (inner >= 1)

        movement = np.sqrt((x-old_x)**2 + (y-old_y)**2)

        reward = 0
        terminated = False

        if on_track:

            reward += 1

            reward += movement*3

            reward += self.car.speed*0.5

            front = self.get_sensor_distance(0)

            if front < 15:
                reward -= 5

            if action == 0:
                reward -= 0.2

        else:

            reward = -100
            terminated = True

        observation = self.get_observation()

        truncated = False

        info = {}

        return observation,reward,terminated,truncated,info

